/**
 * Hyphen yongdo 코드 배열 → Court 트리플 union (중복 제거).
 *
 * 입력 예: ["31", "33"]  (농지 + 임야)
 * 출력: [
 *   { lclCd:"10000", mclCd:"10100", sclCd:"10101" },  // 전
 *   { lclCd:"10000", mclCd:"10100", sclCd:"10102" },  // 답
 *   { lclCd:"10000", mclCd:"10100", sclCd:"10103" },  // 과수원
 *   { lclCd:"10000", mclCd:"10100", sclCd:"10105" },  // 임야
 * ]
 *
 * 빈 입력 = 빈 배열 (= court 측 용도 미지정 1회 호출).
 */
export function mapHyphenYongdoToCourt(yongdoCodes: string[]): CourtUsageTriple[] {
  if (yongdoCodes.length === 0) return [];

  const seen = new Set<string>();
  const out: CourtUsageTriple[] = [];

  for (const yc of yongdoCodes) {
    const triples = HYPHEN_TO_COURT_USAGE[yc];
    if (!triples) continue; // 매핑 없는 코드는 무시
    for (const t of triples) {
      const key = `${t.lclCd}|${t.mclCd}|${t.sclCd}`;
      if (seen.has(key)) continue;
      seen.add(key);
      out.push(t);
    }
  }

  return out;
}
